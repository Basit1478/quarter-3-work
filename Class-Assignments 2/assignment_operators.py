#assignment operators
a = 5
print("Assignment: a = 5                    ",a)  # Output: 5

a += 3  # Equivalent to x = x + 3
print("Addition Assignment: a += 3          ",a)  # Output: 8

a -= 3  # Equivalent to x = x - 3
print("Subtraction Assignment: a -= 3       ",a)  # Output: 5

a *= 3  # Equivalent to x = x * 3
print("Multiplication Assignment: a *= 3    ",a)  # Output: 15

a /= 3  # Equivalent to x = x / 3
print("Division Assignment: a /= 3          ",a)  # Output: 5.0

a //= 3  # Equivalent to x = x // 3
print("Floor Division Assignment: a //= 3   ",a)  # Output: 1.0

a %= 3  # Equivalent to x = x % 3
print("Modulus Assignment: a %= 3          ",a)  # Output: 1.0


