#Comparison (Relational) Operators

#Equal
a = 10
b = 5
print(a == b)

#Not Equal
a = 10
b = 5
print(a != b)

#Greater Than
a = 10
b = 5
print(a > b)

#Less Than
a = 10
b = 5
print(a < b)

#Greater Than or Equal To
a = 10
b = 5
print(a >= b)

#Less Than or Equal To
a = 10
b = 5
print(a <= b)
